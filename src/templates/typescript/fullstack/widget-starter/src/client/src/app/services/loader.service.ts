/* ################################################################### */
/*
/*  Service for managing loader over app (show, close)
/*
/* ################################################################### */

import { Injectable } from '@angular/core';

/* ------------------------------------------------------------------- */
/*                              Config
/* ------------------------------------------------------------------- */

@Injectable({
  providedIn: 'root'
})

/* ------------------------------------------------------------------- */
/*                              Service
/* ------------------------------------------------------------------- */

export class LoaderService {

  /* ------------------------------------------------------------------- */
  /*                              Vars
  /* ------------------------------------------------------------------- */

  private enabled: boolean;

  /* ------------------------------------------------------------------- */
  /*                            Constructor
  /* ------------------------------------------------------------------- */

  public constructor() { }

  /* ------------------------------------------------------------------- */
  /*                              Getter
  /* ------------------------------------------------------------------- */

  public get status() {
    return this.enabled;
  }

  /* ------------------------------------------------------------------- */
  /*                              Show
  /* ------------------------------------------------------------------- */

  public show = () =>
    this.enabled = true

  /* ------------------------------------------------------------------- */
  /*                              Hide
  /* ------------------------------------------------------------------- */

  public hide = () =>
    this.enabled = false

}
